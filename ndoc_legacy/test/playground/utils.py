"""
Utility module for Demo Application.
Contains helper classes and functions.
"""

class Calculator:
    """
    A simple calculator class.
    Used to demonstrate class-level docstring extraction.
    """

    def add(self, a, b):
        """
        Adds two numbers.
        
        Args:
            a (int): First number
            b (int): Second number
            
        Returns:
            int: The sum of a and b
        """
        return a + b

    def subtract(self, a, b):
        """
        Subtracts b from a.
        """
        return a - b
